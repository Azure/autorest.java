package com.azure.autorest.extension.base.util;

/**
 * The log level for logging.
 */
public enum LogLevel {
    /**
     * The log level for errors.
     */
    ERROR,
    /**
     * The log level for warnings.
     */
    WARNING,
    /**
     * The log level for informational.
     */
    INFORMATIONAL,
    /**
     * The log level for verbose.
     */
    VERBOSE,
    /**
     * The log level for nothing.
     */
    NONE
}
